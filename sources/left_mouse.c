#include "rtv1.h"

void	hold_left_button(t_utils *u, int x, int y)
{
	u += 0;
	x += 0;
	y += 0;
}

void	left_button_down(t_utils *u, int x, int y)
{
	u += 0;
	x += 0;
	y += 0;
}

void	left_button_up(t_utils *u)
{
	u += 0;
}
