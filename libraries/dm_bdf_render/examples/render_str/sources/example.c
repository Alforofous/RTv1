#include "dm_bdf_render.h"

void	put_pixel(int x, int y, t_uint color, void *param)
{
	char	**screen;

	screen = (char **)param;
	if (x >= 0 && y >= 0 && x < 65 && y < 32)
	{
		if (color == 0xFF0000)
			ft_putstr("\e[31m");
		screen[y][x] = 'x';
		ft_putstr("\e[0m");
	}
}

void	put_screen(char **screen)
{
	int	i;

	i = 0;
	while (i < 32)
	{
		ft_putendl(screen[i]);
		i++;
	}
}

